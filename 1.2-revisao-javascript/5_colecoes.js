//listas
var lista = [];
lista.push(42); // adiciona 42 (int) no fim
lista.push("String"); // adiciona "String" (string) no fim
lista.push({'doce':'chocolate'}); // adiciona {'doce':'chocolate'} (mapa) no fim
lista.push([1,2,3]); // adiciona [1,2,3] (lista) no fim
lista.pop(); //apaga o último elemento
lista.shift(); // apaga o primeiro elemento
lista.unshift("primeiro"); // adiciona "primeiro" no começo da lista
lista.length; // mostra o tamanho da lista

